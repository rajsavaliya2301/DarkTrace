def _onion(prefix: str = "") -> str:
    """Generate a valid-looking .onion address."""
    # Valid onion characters: a-z, 2-7 (base32 without 0,1,8,9)
    alphabet = string.ascii_lowercase + "234567"
    # Onion v3 addresses are 56 chars
    addr = "".join(secrets.choice(alphabet) for _ in range(56))
    return f"{prefix}{addr}.onion"

